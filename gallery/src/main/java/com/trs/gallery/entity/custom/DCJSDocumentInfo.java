package com.trs.gallery.entity.custom;

public class DCJSDocumentInfo extends DocumentInfo {
	/**
	 * 总登记号
	 */
	private String Serial_Number;
	
	/**
	 * 标题
	 */
	private String Title;
	
	/**
	 * 作者
	 */
	private String Author;
	
	/**
	 * 创作年代
	 */
	private String Creation_age;
	
	/**
	 * 类别
	 */
	private String Category;
	
	/**
	 * 规格
	 */
	private String Specifications;
	
	/**
	 * 作者简介
	 */
	private String Introduction;
	
	/**
	 * 收藏时间
	 */
	private String Collection_Time;
	
	/**
	 * 作品图片
	 */
	private String Image;
	
	/**
	 * 材质
	 */
	private String Material;

	public String getSerial_Number() {
		return Serial_Number;
	}

	public void setSerial_Number(String serial_Number) {
		Serial_Number = serial_Number;
	}

	public String getTitle() {
		return Title;
	}

	public void setTitle(String title) {
		Title = title;
	}

	public String getAuthor() {
		return Author;
	}

	public void setAuthor(String author) {
		Author = author;
	}

	public String getCreation_age() {
		return Creation_age;
	}

	public void setCreation_age(String creation_age) {
		Creation_age = creation_age;
	}

	public String getCategory() {
		return Category;
	}

	public void setCategory(String category) {
		Category = category;
	}

	public String getSpecifications() {
		return Specifications;
	}

	public void setSpecifications(String specifications) {
		Specifications = specifications;
	}

	public String getIntroduction() {
		return Introduction;
	}

	public void setIntroduction(String introduction) {
		Introduction = introduction;
	}

	public String getCollection_Time() {
		return Collection_Time;
	}

	public void setCollection_Time(String collection_Time) {
		Collection_Time = collection_Time;
	}

	public String getImage() {
		return Image;
	}

	public void setImage(String image) {
		Image = image;
	}

	public String getMaterial() {
		return Material;
	}

	public void setMaterial(String material) {
		Material = material;
	}
	
	
}
